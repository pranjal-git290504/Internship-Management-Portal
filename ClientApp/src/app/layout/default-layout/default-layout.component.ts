export class DefaultLayoutComponent implements OnInit {
  public sideMenuItems: INavDataExtended[] = [];
  constructor(private authService: AuthService) {} 
  ngOnInit(): void {
    this.sideMenuItems = navItems.filter((item) => {
      return !item.role || this.authService.hasRole(item.role);
    });

    console.log('logged in user userId', this.authService.getUserId());
  }
}
